/*
ввести вещественные числа X и Y. 
Определить принадле¬жит ли точка с координатами (X, Y) заштрихованной части плоскости. 
Протестировать все ветви алгоритма.
*/

#include <stdio.h>

int main(void) {
    double x, y;

    printf("Введите два числа (x y):\n");
    scanf("%lf %lf", &x, &y);

    double rRight = x*x + y*y;                 
    double rLeft  = (x + 1.0)*(x + 1.0) + y*y; 

    if (rRight > 1.0) {
        if (rLeft > 1.0) {
            printf("Точка не принадлежит заштрихованной области: вне обоих кругов (x^2+y^2>1 и (x+1)^2+y^2>1).\n");
        } else {
            printf("Точка не принадлежит заштрихованной области: вне правого круга (x^2+y^2>1).\n");
        }
    } else {
        if (rLeft > 1.0) {
            printf("Точка не принадлежит заштрихованной области: вне левого круга ((x+1)^2+y^2>1).\n");
        } else {
            printf("Точка принадлежит заштрихованной области (в пересечении кругов).\n");
        }
    }

    return 0;
}
